    private void btGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardarActionPerformed
         if(evt.getSource()==btGuardar)
      {
         if (rfNit.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Ingrese un Nit");
        }
        else
        {
            if(evt.getSource()==btGuardar)
            {
                if (buscarNit(rfNit.getText())== false)
                    {
                     ClaseEmpleado emp= new ClaseEmpleado();
                     emp.setNit(rfNit.getText());
                     emp.setNonbre(tfNombre.getText());
                     emp.setDireccion(tfDireccion.getText());
                     emp.setTelefono(tfTelefono.getText());
                     emp.setCorreo(tfCorreoElectronico.getText());
                     ArregloEmpleado.add(emp);
                     rfNit.setText("");
                     tfNombre.setText("");
                     tfDireccion.setText("");
                     tfTelefono.setText("");
                     tfCorreoElectronico.setText("");

                    }
                    else
                    {
                        JOptionPane.showMessageDialog(null, "Nit ya existe.");
                        rfNit.setText("");
                        tfNombre.setText("");
                        tfTelefono.setText("");
                        tfDireccion.setText("");
                        tfCorreoElectronico.setText("");
                                                

                                                

                    }            
            }
        }        

          
      }   
      }
      
       
        private boolean buscarNit(String n){
        boolean encontrado = false;
        ClaseEmpleado empl3;
        Iterator<ClaseEmpleado>itr=ArregloEmpleado.iterator();
        while(itr.hasNext())
        {
            empl3=itr.next();
            
            if (n.compareTo(empl3.getNit())==0)
            {
                encontrado=true;
            }
        }
        return encontrado;
 
    }//GEN-LAST:event_btGuardarActionPerformed

